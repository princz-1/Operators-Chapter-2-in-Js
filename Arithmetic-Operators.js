/* Arithmetic Operators
+,-,*,/
- Modulus :- %
- Exponentiation
-  Increment (Pre-Increment & Post-Increment)
- Decrement (Pre-Decrement & Post-Decrement)
*/

let a = 5;
let b = 2;

console.log("a =",a,"b =",b); // a = 5 b = 2
console.log("a + b =",a+b); // a + b = 7
console.log("a - b =",a-b); // a - b = 3
console.log("a * b =",a*b); // a * b = 10
console.log("a / b =",a/b); // a / b = 2.5
console.log("a % b =",a%b); // a % b = 1
console.log("a ** b =",a**b); //5^2 = 25



// - Increment
let c = 25;
c++;
console.log("c = ",c);// c = 25
// Pre-increment
let e = 23;
console.log("++e =",++e); // ++e = 24
//Post-increment
let f = 4;
console.log("f++ =",f++);// f++ = 4
console.log("f++ =",f); // f++ = 5



// - Decrement
let d = 6;
--d;
console.log("d = ",d); // d = 5
// Pre-decrement
let g = 45;
console.log("--g =",--g);// --g = 44
// Post-decrement
let h = 65;
console.log("h-- =",h--); // h-- = 65
console.log("h-- =",h); //h-- = 64

