/* Comparison Operators 

- Equal to (==)
- Equal to & type (===)
- Not equal to  (!=) 
- Not equal to & type (!==)
- Grater then (>)
- Grater then equal to (>=)
- Less then (<)
- Less then equal to (<=)


*/


// Equal to (==)
let a = 5;
let b = 5;
console.log("a == b",a == b); // True

// Equal to & type (===)
let c = 5;
let d = "5";
console.log("c === d", c === d);// False

//  Not equal to  (!=) 
let e = 5;
let f = 5;
console.log("e != f", e != f); // False

// Not equal to & type (!==)
let g = 5;
let h = "5";
console.log("g !== h", g !== h); // True

// Greter then (>)
let i = 5;
let j = 3;
console.log("i > j",i > j); // True

// Greter then equal to (>=)
let k = 5;
let l = 3;
console.log("k >= l",k >= l); // True


// Less then (<)
let m = 5;
let n = 3;
console.log("m < n",m < n); // False

// Less then equal to (<=)
let o = 5;
let p = 3;
console.log("o <= p",o <= p); // False
